my_fav_fruits = {'apple', 'mango', 'banana'}
my_bestie_fav_fruits = {'orange', 'watermelon', 'cherry'}

#union result
union_results = my_fav_fruits.union(my_bestie_fav_fruits)

#interection results
intersection_results = my_fav_fruits.intersection(my_bestie_fav_fruits)

#difference results
difference_results = my_fav_fruits - my_bestie_fav_fruits
#print results
print('Union :', union_results)
print('Intersection :', intersection_results)
print('Difference :', difference_results)
